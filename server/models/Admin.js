import mongoose from "mongoose";

const AdminSchema = new mongoose.Schema({
    firstName: {
        type: String,
        required: true,
    },
    lastName: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    },
    role: {
        type: String,
        default: "admin",
        required: false,
    },
});
const AdminModel = mongoose.model("admin", AdminSchema);

export default AdminModel;

